/*IPHONE 5*/

$(function(){
	var stickyHeaderTop = $('header').offset().top;

		$(window).scroll(
			function(){
			if( $(window).scrollTop() >= 1600 && $(window).scrollTop() <= 1850) 
			{
	            $('#bir').addClass("iphone5");
	            $('#bir_1').addClass("iphone5_1");
	                
	        } else 
	        {	 
				$('#bir').removeClass("iphone5");
				$('#bir_1').removeClass("iphone5_1");   
	        }
		});
	});

